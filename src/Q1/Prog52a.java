public class Prog52a {
    public static void main(String[] args){
        // java types; int, double, boolean and string
        int len = 143;
        int wid = 80;
        wid = 82;

        // math operators + - * / and % = mod
        int area = len * wid;
        int perim = 2 * len + 2 * wid;
        System.out.println("area = " + area);
        System.out.println("Perim = " + perim);
    }
}
